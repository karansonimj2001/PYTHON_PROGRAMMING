#Assignment Operators
#var=value/variable/expression

a=4
b=a
c=a+b
print(a)
print(b)
print(c)


#Simple Assignment
a=10
print(a)

#Multiple Assignment
a,b=4,5
print(a)
print(b)

#Chained Assignment
a=b=c=10
print(a)
print(b)
print(c)

#Compound Assignment(Arithmetic,Assignment)
a=2
print(a)
a+=5   #a=a+5
print(a)

a*=3
print(a)


#Expression Assignment(:=) #from 3.8 (walrus operator)

print(x:=5+4)
print(x+1)












