a="Good"
b="Morning"

#String concatenation
#str=str+str
#returns a new string after concatenation
print(a+b)
print(a+" "+b)

#String repeatation
#str=str*int
#returns a new string after repeatation
print(a*3)

#String Searching(using membership operator(in))
#bool=substr in str
#returns True if substr is present in str,else False
print('d' in a)
print('d' in b)
print('in' in b)
print('ring' in b)

#String length
#int=len(str)
#returns no. of chars present in given string
print(len(a))
print(len("a b"))
