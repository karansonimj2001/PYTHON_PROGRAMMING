s="dUcaT iNdIa"
print(s)
print(s.lower())#returns a new copy of lowecase str
print(s.upper())#returns a new copy of uppercase str
print(s.capitalize())#returns a new copy of capitalizecase str
#capitalizecase--->only first in upper and rest in lower
print(s.swapcase())#returns a new copy of swapcase str
#swapcase
    #lower--->upper
    #upper--->lower
print(s.title())
#title
    #first char in each word in upper
    #rest in lower

print(s.islower())#returns True if all chars in lowercase,else False
print(s.isupper())#returns True if all chars in upper,else False

